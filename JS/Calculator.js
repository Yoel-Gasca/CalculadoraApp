class Calculator {
    //Basic operators
    addition(num1, num2){
        return num1 + num2;
    }

    substraction(num1, num2){
        return num1 - num2;
    }

    multiplication(num1, num2){
        return num1 * num2;
    }

    division(num1, num2){
        return num1 / num2;
    }

    
}